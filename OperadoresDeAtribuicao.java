public class OperadoresDeAtribuicao {
	public static void main(String[] args) { 
		 int numero = 5; 

		 numero += 3; // Equivalente o numero = numero + 3;
		 numero -= 2; // Equivalente o numero = numero - 2;
		 numero *= 4; // Equivalente o numero = numero * 4;
		 numero /= 2; // Equivalente o numero = numero / 2;

   
     System.out.println("Valor final: " + numero);
     
   }
}